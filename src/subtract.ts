/**
 * Subtract two numbers.
 *
 * @category Math
 *
 * First version: July 15, 2017
 * Last updated : July 15, 2017
 *
 * @export
 * @param {number} inputA
 * @param {number} inputB
 * @returns {number}
 */

export function subtract(inputA: number, inputB: number): number {
  return inputA - inputB;
}
